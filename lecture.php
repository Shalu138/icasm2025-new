    <p >But it was not until 1946 that Civil Aviation actually called on him. It was at
                    the time
                    when the
                    pilots of the Royal Air Force Belgian section were dismissed, and President Périer charged him
                    with the development and direction of the Medical Service of Sabena, which was undergoing a
                    period of great expansion. While carrying out that assignment, Dr. Allard fully complied with
                    his President’s instructions, <i>“Excellence must be the keyword for an Airline Medical
                        Service”.</i></p>
                <p >From that moment on, he became acquainted with Aerospace Medicine problems
                    through the
                    Royal Air Force medical publications, mainly after reading the l944 edition of Harry
                    Armstrong’s book.</p>
                <p >He developed an interest in the studies of Aeronautical Physiology and the
                    application
                    of
                    preventive care, already used in Military Aviation, to Civil Commercial Aviation.</p>
                <p >Each year , he would attend, in the USA, the annual convention of their society,
                    at that
                    moment
                    called AeroMedical Association, and this convinced him that a similar association must exist in
                    Europe. Thus, he became co-founder of the French Branch of the AeroMedical Association that
                    met for the first time in Paris in 1952, and again in Brussels the following year with Dr. Allard as
                    Secretary General.</p>
                <p >Other meetings followed each year - Zurich in 1954, Paris again in 1955,
                    Scheveningen in
                    1956,
                    Stockholm in 1957, but it was not until 1958 that Dr. Allard, then the Secretary General, became
                    the driving force that organized in Louwain (Belgium), the First World Congress of Aviation
                    Medicine, attended by His Majesty King Baudouin.</p>
                <p >Even though he had only joined the Academy in 1958, the prestige he earned with
                    the
                    success of
                    this 1 st World Congress, led the Academy Council to invite him already as Secretary General in
                    1959.</p>
                <p >From that date on, Dr. Allard’s international fame never ceased to grow. His
                    prestige
                    and the
                    name of the Academy developed together. The Academy Congresses were then held not only in
                    Europe but also in other regions, starting in Israel in 1971, and Lebanon in 1974.</p>
                <p >His intelligence, leadership ability, common sense and communication skills won
                    him
                    friends in
                    many countries, and he played a major role in extending the activities of the Academy to every
                    Continent. The Membership included 36 countries, and the Academy was recognized by every
                    national Aviation Medical Society, and began receiving requests for sponsorship of the
                    International Congresses, which were then held every year, alternatively in Europe and outside
                    Europe.</p>
                <p >During his eleven years as Secretary General, and even later, Dr. Allard was the
                    spirit
                    of those
                    Congresses where he would regularly submit the clinical data of his service.</p>
                <p >Beyond his excellent medical qualifications and thorough knowledge in the field
                    of Aerospace
                    Medicine, Dr. Allard was a man of extensive learning, and his presence was very much in
                    demand. On the Belgian TV he was the regular commentator for the American and Soviet
                    Programs, and this made him very popular in his country.</p>
                <p >In spite of all the respect bestowed upon him by everyone that knew him, Dr.
                    Allard was always
                    modest, informal and discreet.</p>
                <p >The Service of his Country, his Company and the Academy were like a mission to
                    him, and he
                    would never profit from his international prestige to his personal advantage. Nor would he ever
                    accept that his functions as Secretary General be remunerated in any form whatsoever.</p>
                <p >Dr. Allard was always available for cooperation with less experienced colleagues
                    and, mainly in
                    Europe, he became the favorite adviser of airline doctors. He loved to help them, namely the
                    youngest. He was generous, but discreet and easy to approach.</p>
                <p >Both as Member and President of IATA Medical Committee, since its creation after
                    World War
                    II, he participated in the preparing of the text and the publication of the first Medical Manual
                    that
                    laid down the principles required for the structures, procedures and equipment of airline Medical
                    Departments, including rules for the flying Personnel selection and survey.</p>
                <p >I replaced him there in 1969, and I carry today the then IATA tie that was his,
