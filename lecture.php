    <p>The André Allard Memorial Lecture was instituted in 1980 to honor Dr. André Allard, the seventh President of the Academy (1971 - 1973).</p>
    <p>At the 41st ICASM in Hamburg in September 1993, in his introduction to the lecturer, Dr. Antonio Castelo-Branco gave a brief biography of Dr. Allard, a most important figure in the Academy’s history.</p>
    <p>The text of his address is included here as an introduction to the record of past Allard Memorial Lectures.</p>

    <p style="font-size:27px;" class="mt-4"><strong>Dr. André Allard</strong></p>
    <p>Dr. André Allard was the President of the Academy from 1971 until 1973 and the Secretary General from 1959 until 1970.</p>
    <p>It is obvious that his was an extremely long office and it was very important for the consolidation of the activities of the Academy as the Association had had in 1955 a very ill-balanced beginning and Dr. Allard’s actions reinforced its foundations, attracted delegates from 36 countries and confirmed its international prestige in the field of Aerospace Medicine.</p>
  <p>In 1937, when Dr. Allard obtained with the highest marks his Diploma in Medicine from the Free University of Brussels, no one could anticipate his future devotion to Aerospace Medicine.

His University studies qualified him to be a surgeon, and in fact, from 1938 until 1946 he was a residing surgeon at the Brussels University Hospital.</p>
