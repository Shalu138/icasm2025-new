        <p><strong>Defining the future of global systems governing health and wellbeing of populations: Amidst the revolution in aviation and space travel</strong></p>
        <p>The global system of governance established post World War II, alongside the Bretton Woods Institutions, are currently under threat. The impending breakdown of these systems - originally established to maintain peace, security and economic growth - indicates that the world is at risk once again; not just to conflicts and wars, but also pandemics, climate destabilisation, and the dangerous rise of uncontrolled nationalism and short-termism.</p>
        <p>What is often overlooked is that the global movement of people and goods requires global standards that are respected and maintained by international bodies including the International Civil Aviation Organisation and the International Air Transport Association, established in 1944 and 1945 respectively. Aviation global standards were under threat during the Covid-19 pandemic when governments worldwide unilaterally decided to close borders and restrict the movement of essential supplies and people.</p>
        <p>Amidst the revolution in aviation and space travel - with urban air mobility, hydrogen-powered aircrafts and commercial space travel becoming a reality in the near future, agreement on international standards and regulations governing both aviation and space safety is ever more important. In this lecture, I will explore the future of global health governance and diplomacy, amidst the current megatrends shaping the world. The world will need to reach a consensus on the international regulations necessary for the future of aviation.</p>
